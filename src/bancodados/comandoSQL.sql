
-- CRIA A TABELA FUNCIONÁRIOS.
CREATE TABLE FUNCIONARIOS 
(
    IDFUNCIONARIO INTEGER GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1), 
    -- DADOS PESSOAIS;
    NOMEFUNCIONARIO VARCHAR(40), 
    CPFFUNCIONARIO VARCHAR(20) UNIQUE,
    DATANASCIMENTOFUNCIONARIO DATE,
    PISFUNCIONARIO VARCHAR(20),    
    TELEFONEFUNCIONARIO VARCHAR(20), 
    CELULARFUNCIONARIO VARCHAR(20),
    CEPFUNCIONARIO VARCHAR(20),
    RUAAVNFUNCIONARIO VARCHAR(90),   
    NUMEROFUNCIONARIO VARCHAR(8),
    BAIRROFUNCIONARIO VARCHAR(80),
    CIDADEFUNCIONARIO VARCHAR(80), 
    ESTADOFUNCIONARIO VARCHAR(80), 
    -- DADOS FUNCIONAIS;
    DATAADIMISSAOFUNCIONARIO DATE, 
    SITUACAOFUNCIONARIO VARCHAR(40), 
    DATADEMISSAOFUNCIONARIO DATE,
    DATAAFASTAMENTOFUNCIONARIO DATE,
    DATARETORNOFUNCIONARIO DATE, 
    CARGOFUNCIONARIO VARCHAR(80),
    NIVELPERMISSAOFUNCIONARIO VARCHAR(40),
    NOMEUSUARIOFUNCIONARIO VARCHAR(40),
    SENHAFUNCIONARIO VARCHAR(80),
    PRIMARY KEY (IDFUNCIONARIO)
);


