-- CRIAR TABELA PRODUTOS [MATERIA PRIMA(TOMATE,QUEIJO...) OU NÃO(COCA-COLA,ENTRE OUTROS)]
CREATE TABLE PRODUTOSMATERIA
(
    IDPRODUTO INTEGER GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1),
    NOMEPRODUTO VARCHAR(80),
    UNIDADEMEDIDAPRODUTO VARCHAR(20),
    TIPOPRODUTO VARCHAR(20),
    PRIMARY KEY(IDPRODUTO)
);

-- CRIAR TABELA PRODUTOS PARA CONTROLE NO ESTOQUE.
CREATE TABLE PRODUTOESTOQUE
(
    IDPRODUTOESTOQUE INT GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1),
    IDPRODUTOMATERIA INT,
    NOMEPRODUTOESTOQUE VARCHAR(80), 
    QUANTIDADECOMPRADAPRODUTOESTOQUE DECIMAL,
    DATACOMPRAPRODUTOESTOQUE DATE,
    DATAVALIDADEPRODUTOESTOQUE DATE,
    -- PRIMARY KEY(idProduto),
    FOREIGN KEY (IDPRODUTOMATERIA) REFERENCES PRODUTOSMATERIA (IDPRODUTO)
);

